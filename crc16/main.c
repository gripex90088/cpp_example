uint16_t CRC16(uint8_t * Data, uint16_t Length)
{
	uint16_t crc;
	uint8_t da;
	crc = 0xFFFF;
	while(Length--!=0)
	{
		da=(uint8_t) (crc/256);
		crc <<= 8;
		crc ^= crc16_table[da^*Data];
		Data++;
	}
	return crc;
}


