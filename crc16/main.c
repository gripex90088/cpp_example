uint16_t crc16_ccitt(uint8_t *buf, uint16_t len)
{
    uint16_t i, j;
    uint16_t crc_reg = 0xFFFF;
    uint16_t crc_val;

    for (i = 0; i < len; i++)
    {
        crc_val = buf[i] << 8;
        for (j = 0; j < 8; j++)
        {
            if (((int16_t)(crc_reg ^ crc_val)) < 0)
            crc_reg = (crc_reg << 1) ^ 0x1021;
            else
            crc_reg <<= 1;
            crc_val <<= 1;
        }
    }
    return crc_reg;
}

